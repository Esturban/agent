# Problems chosen because they have a non-obvious solving strategy that becomes
# clear once you recall an analogous problem. The snail/wall problem is a classic
# example where the solver must notice the "last-day exception" — the snail
# reaches the top before sliding back on the final day. Analogy helps prime this.

SAMPLE_PROBLEMS = [
    (
        "A snail climbs 3 feet up a wall during the day and slides 2 feet back "
        "each night. The wall is 10 feet tall. How many days does it take to "
        "reach the top?"
    ),
    (
        "Two trains start from cities 300 miles apart, traveling toward each other. "
        "Train A goes 60 mph and Train B goes 90 mph. A fly starts at Train A and "
        "flies back and forth between the two trains at 150 mph until they collide. "
        "How far does the fly travel total?"
    ),
    (
        "You have a 3-gallon jug and a 5-gallon jug with no markings. "
        "How do you measure exactly 4 gallons of water?"
    ),
]
